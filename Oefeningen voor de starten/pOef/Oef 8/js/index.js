let getal1 = parseFloat(window.prompt('Geef hier de eerste getal in'));
let getal2 = parseFloat(window.prompt('Geef hier de tweede getal in'));

if
    (getal1 > getal2)
{
        alert("getal 1 is groter dan getal 2");
}
else if
    (getal1 == getal2)
{
        alert("getal 1 is gelijk aan getal 2");
}
else if
    (getal1 < getal2)
{
        alert("getal 1 is kleiner aan getal 2");
}